function t(k) {
  const l = state?.settings?.lang || 'nl';
  return I18N[l]?.[k] || I18N.nl[k] || k;
}

function typeLabel(k) {
  const l = state?.settings?.lang || 'nl';
  return (TYPE_LABELS[l] || TYPE_LABELS.nl)[k] || k;
}

function fmtMoney(v) {
  const l = state?.settings?.lang || 'nl';
  return new Intl.NumberFormat(
    l === 'de' ? 'de-DE' : l === 'pl' ? 'pl-PL' : 'nl-NL',
    { style: 'currency', currency: 'EUR' }
  ).format(v || 0);
}

function fmtDate(d) {
  const l = state?.settings?.lang || 'nl';
  return new Intl.DateTimeFormat(
    l === 'de' ? 'de-DE' : l === 'pl' ? 'pl-PL' : 'nl-NL'
  ).format(d);
}
